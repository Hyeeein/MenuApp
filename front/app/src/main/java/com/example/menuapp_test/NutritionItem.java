package com.example.menuapp_test;

public class NutritionItem {
    private int id;
    private String name;
    private String gram;
    private String calorie;
    private String carbohydrate;
    private String protein;
    private String fat;
    private String saturatedfat;
    private String unsaturatedfat;
    private String cholesterol;
    private String sodium;
    private String potash;
    private String ingredient;
    private String allergy;
    private String menu;


    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getGram() {
        return gram;
    }

    public void setGram(String gram) {
        this.gram = gram;
    }

    public String getCalorie() {
        return calorie;
    }

    public void setCalorie(String calorie) {
        this.calorie = calorie;
    }

    public String getCarbohydrate() {
        return carbohydrate;
    }

    public void setCarbohydrate(String carbohydrate) {
        this.carbohydrate = carbohydrate;
    }

    public String getProtein() {
        return protein;
    }

    public void setProtein(String protein) {
        this.protein = protein;
    }

    public String getFat() {
        return fat;
    }

    public void setFat(String fat) {
        this.fat = fat;
    }

    public String getSaturatedfat() {
        return saturatedfat;
    }

    public void setSaturatedfat(String saturatedfat) {
        this.saturatedfat = saturatedfat;
    }

    public String getUnsaturatedfat() {
        return unsaturatedfat;
    }

    public void setUnsaturatedfat(String unsaturatedfat) {
        this.unsaturatedfat = unsaturatedfat;
    }

    public String getCholesterol() {
        return cholesterol;
    }

    public void setCholesterol(String cholesterol) {
        this.cholesterol = cholesterol;
    }

    public String getSodium() {
        return sodium;
    }

    public void setSodium(String sodium) {
        this.sodium = sodium;
    }

    public String getPotash() {
        return potash;
    }

    public void setPotash(String potash) {
        this.potash = potash;
    }

    public String getIngredient() {
        return ingredient;
    }

    public void setIngredient(String ingredient) {
        this.ingredient = ingredient;
    }

    public String getAllergy() {
        return allergy;
    }

    public void setAllergy(String allergy) {
        this.allergy = allergy;
    }

    public String getMenu() {
        return menu;
    }

    public void setMenu(String menu) {
        this.menu = menu;
    }
}
